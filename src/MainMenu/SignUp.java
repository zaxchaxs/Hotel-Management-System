    private void emailFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_emailFieldKeyReleased
        emailField.setText(emailField.getText().toLowerCase());
        
        int a=emailField.getText().indexOf('@');
        int b=emailField.getText().length();
        
        if(a == -1){
          emailLabel.setText("Invalid Email");
        } else if(b > a+1) {
            String s = emailField.getText();
            String[] splitString = s.split("@");
            if(splitString[1].equalsIgnoreCase("gmail.com")){
                emailLabel.setText("");
                nameField.requestFocus();
            } else {
                emailLabel.setText("Invalid Email");
            };
        };

        if(emailField.getText().equals("")) emailLabel.setText("");
    }//GEN-LAST:event_emailFieldKeyReleased

