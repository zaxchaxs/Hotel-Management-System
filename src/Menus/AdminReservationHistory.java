    public void showDataTable(){
        String[] headerTableColumns = {"ID", "Name", "Email", "Phone Number", "Room ID", "Room Name", "Type", "Check in Date", "Check out Date"};
        Object[][] customerValue = new Object[listCustomerHistory.size()][headerTableColumns.length];
        
        int i = 0;
        for(Customer customer: listCustomerHistory) {
            String roomData[] = {customer.id+"", customer.name, customer.email, customer.phoneNumber, customer.roomId, customer.roomName, customer.roomType, customer.checkInDate, customer.checkOutDate, customer.status};
            customerValue[i] = roomData;
            i++;
        };
        i = 0;
        
        listCustomerTableModel = new DefaultTableModel(customerValue, headerTableColumns) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            };        
        };
        
        resultTable.setModel(listCustomerTableModel);
    };    
    
