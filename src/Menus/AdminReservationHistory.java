    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        
        int confirm = JOptionPane.showConfirmDialog(null, "Are you sure want to delete \nall reservation history?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
        if(confirm == JOptionPane.YES_OPTION) {
           
            try {
                DatabaseResultResponse response = db.deleteAllCustomer();
                if(response.status == 200) {
                    JOptionPane.showMessageDialog(this, "Success deleting \nall reservation history!");
                    getCustomerHistoryData();
                } else {
                    JOptionPane.showMessageDialog(this, response.message);
                };
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        } else {
            return;
        }
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        int selectedRow = resultTable.getSelectedRow();
        if(selectedRow <0) {
            JOptionPane.showMessageDialog(this, "Please Select customer history to delete");
            return;
        };
        
        String name = resultTable.getValueAt(selectedRow, 1).toString();
        int confirm = JOptionPane.showConfirmDialog(null, "Are you sure want to delete " + name + "\nreservation history?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
        if(confirm == JOptionPane.YES_OPTION) {
            String idCustomer = resultTable.getValueAt(selectedRow, 0).toString();
            try {
                DatabaseResultResponse response = db.deleteCustomer(idCustomer);
                if(response.status == 200) {
                    JOptionPane.showMessageDialog(this, "Success deleting " + name + " reservation history!");
                    getCustomerHistoryData();
                } else {
                    JOptionPane.showMessageDialog(this, response.message);
                };
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        } else {
            return;
        }
    }//GEN-LAST:event_jButton7ActionPerformed

